def build_articles():
    """Convert ARTICLES list to full article objects."""
    result = []
    for a in ARTICLES:
        article = {
            "law": LAW_NAME,
            "article_number": a["num"],
            "text": a["text"],
            "id": f"{PREFIX}_{a['num']}",
            "chunk_index": a["num"],
            "chapter": a["chapter"],
            "section": "",
            "topic": a["topic"],
            "topic_tags": a["tags"],
            "has_deadline": a["has_deadline"],
            "deadline_details": a["deadline"],
            "source_pages": SOURCE,
        }
        result.append(article)
    return result


def main():
    """Add electronic evidence procedures to articles.json."""
    articles_path = os.path.join(os.path.dirname(__file__), "articles.json")

    # Load existing
    with open(articles_path, "r", encoding="utf-8") as f:
        data = json.load(f)

    existing_ids = {a["id"] for a in data["articles"]}
    new_articles = build_articles()

    added = 0
    for article in new_articles:
        if article["id"] not in existing_ids:
            data["articles"].append(article)
            existing_ids.add(article["id"])
            added += 1

    # Save
    with open(articles_path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    total = len(data["articles"])
    print(f"✅ Added {added} articles from {LAW_NAME}")
    print(f"📊 Total articles: {total}")


if __name__ == "__main__":
    main()
