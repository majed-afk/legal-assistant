def get_article_metadata(num):
    """Get chapter/section/topic for article number."""
    for start, end, chapter, section, topic, tags in ARTICLE_MAP:
        if start <= num <= end:
            return chapter, section, topic, tags
    return "غير محدد", "غير محدد", "إثبات", ["إثبات"]


def parse_articles():
    """Parse law text into individual articles."""
    articles = []
    # Split by "المادة" pattern
    parts = re.split(r'(المادة [^\n]+)', EVIDENCE_LAW_TEXT)

    current_title = None
    for part in parts:
        part = part.strip()
        if not part:
            continue
        if part.startswith("المادة "):
            current_title = part
        elif current_title:
            # Try to get article number from title
            title_text = current_title.replace("المادة ", "")
            num = ORDINAL_MAP.get(title_text, None)
            if num:
                full_text = f"نظام الإثبات - {current_title}\n{part}"
                articles.append((num, full_text))
            current_title = None

    return articles


