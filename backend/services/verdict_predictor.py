def _get_client() -> anthropic.Anthropic:
    if not ANTHROPIC_API_KEY:
        raise ValueError("ANTHROPIC_API_KEY غير مُعَد")
    return anthropic.Anthropic(api_key=ANTHROPIC_API_KEY)


def stream_verdict_prediction(
    case_text: str,
    context: str,
    case_type: str = "عام",
) -> Generator[str, None, None]:
    """Stream verdict prediction token-by-token using Claude API."""
    client = _get_client()

    # Truncate very long case details
    max_chars = 10000
    if len(case_text) > max_chars:
        case_text = case_text[:max_chars] + "\n\n... [تم اقتطاع بقية التفاصيل]"

    user_message = f"""## تفاصيل القضية:
{case_text}

---

## نوع القضية المُكتشف: {case_type}

---

## 📚 المواد النظامية المسترجعة:
{context}

---

⛔ حلّل القضية وتنبّأ بالحكم المتوقع استناداً حصرياً للمواد النظامية المرفقة. لا تذكر مواد غير مقدمة لك."""

    messages = [{"role": "user", "content": user_message}]

    max_retries = 3
    for attempt in range(max_retries):
        try:
            with client.messages.stream(
                model=CLAUDE_MODEL,
                max_tokens=4000,
                system=SYSTEM_PROMPT_VERDICT,
                messages=messages,
            ) as stream:
                for text in stream.text_stream:
                    yield text
            return
        except anthropic.RateLimitError:
            if attempt < max_retries - 1:
                wait = 2 ** attempt * 5
                log.warning("Rate limited, retrying in %ds (attempt %d)", wait, attempt + 1)
                time.sleep(wait)
            else:
                raise
        except anthropic.APIStatusError as e:
            if e.status_code == 529 and attempt < max_retries - 1:
                wait = 2 ** attempt * 5
                log.warning("API overloaded (529), retrying in %ds", wait)
                time.sleep(wait)
            else:
                raise
