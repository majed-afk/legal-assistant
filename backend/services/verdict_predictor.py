def detect_case_type(text: str) -> str:
    """Detect case type from text using keyword patterns."""
    scores: dict[str, int] = {}
    text_sample = text[:5000]

    for case_type, patterns in CASE_TYPE_PATTERNS.items():
        score = 0
        for pattern in patterns:
            matches = re.findall(pattern, text_sample)
            score += len(matches)
        if score > 0:
            scores[case_type] = score

    if not scores:
        return "عام"

    return max(scores, key=scores.get)


